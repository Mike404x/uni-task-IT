def fill_database():
    print("‚è≥ –°–≤—ä—Ä–∑–≤–∞–Ω–µ —Å –±–∞–∑–∞—Ç–∞ –¥–∞–Ω–Ω–∏...")
    conn = sqlite3.connect("university.db")
    cur = conn.cursor()
    
    # 1. –ü–†–ï–ü–û–î–ê–í–ê–¢–ï–õ–ò (5 –±—Ä–æ—è)
    # –ò–º–µ–Ω–∞—Ç–∞ –∏ —Ç–∏—Ç–ª–∏—Ç–µ —Å–∞ –ø—Ä–∏–º–µ—Ä–Ω–∏
    profs = [
        ("–ò–≤–∞–Ω –î–∏–º–∏—Ç—Ä–æ–≤", "–î–æ—Ü–µ–Ω—Ç"),
        ("–ú–∞—Ä–∏—è –ì–µ–æ—Ä–≥–∏–µ–≤–∞", "–ü—Ä–æ—Ñ–µ—Å–æ—Ä"),
        ("–°—Ç–µ—Ñ–∞–Ω –ù–∏–∫–æ–ª–æ–≤", "–ì–ª. –ê—Å–∏—Å—Ç–µ–Ω—Ç"),
        ("–ï–ª–µ–Ω–∞ –ü–µ—Ç—Ä–æ–≤–∞", "–î–æ—Ü–µ–Ω—Ç"),
        ("–ì–µ–æ—Ä–≥–∏ –•—Ä–∏—Å—Ç–æ–≤", "–ü—Ä–æ—Ñ–µ—Å–æ—Ä")
    ]
    
    print("üëâ –î–æ–±–∞–≤—è–Ω–µ –Ω–∞ –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª–∏...")
    cur.executemany("INSERT INTO professors (name, title) VALUES (?, ?)", profs)
    
    # –í–∑–∏–º–∞–º–µ ID-—Ç–∞—Ç–∞ –Ω–∞ –Ω–æ–≤–∏—Ç–µ –ø—Ä–æ—Ñ–µ—Å–æ—Ä–∏, –∑–∞ –¥–∞ –≥–∏ –ø–æ–ª–∑–≤–∞–º–µ –∑–∞ –∫—É—Ä—Å–æ–≤–µ—Ç–µ
    cur.execute("SELECT id FROM professors")
    prof_ids = [row[0] for row in cur.fetchall()]

    # 2. –î–ò–°–¶–ò–ü–õ–ò–ù–ò (7 –±—Ä–æ—è)
    # –°–≤—ä—Ä–∑–≤–∞–º–µ –≥–∏ —Å–ª—É—á–∞–π–Ω–æ —Å –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª–∏
    courses = [
        ("–ë–∞–∑–∏ –æ—Ç –î–∞–Ω–Ω–∏", random.choice(prof_ids)),
        ("–ü—Ä–æ–≥—Ä–∞–º–∏—Ä–∞–Ω–µ –Ω–∞ Python", random.choice(prof_ids)),
        ("–û–±–µ–∫—Ç–Ω–æ-–û—Ä–∏–µ–Ω—Ç–∏—Ä–∞–Ω–æ –ü—Ä–æ–≥—Ä–∞–º–∏—Ä–∞–Ω–µ", random.choice(prof_ids)),
        ("–ö–æ–º–ø—é—Ç—ä—Ä–Ω–∏ –ú—Ä–µ–∂–∏", random.choice(prof_ids)),
        ("–£–µ–± –î–∏–∑–∞–π–Ω", random.choice(prof_ids)),
        ("–ê–ª–≥–æ—Ä–∏—Ç–º–∏ –∏ –°—Ç—Ä—É–∫—Ç—É—Ä–∏", random.choice(prof_ids)),
        ("–ò–∑–∫—É—Å—Ç–≤–µ–Ω –ò–Ω—Ç–µ–ª–µ–∫—Ç", random.choice(prof_ids))
    ]
    
    print("üëâ –î–æ–±–∞–≤—è–Ω–µ –Ω–∞ –¥–∏—Å—Ü–∏–ø–ª–∏–Ω–∏...")
    cur.executemany("INSERT INTO courses (name, professor_id) VALUES (?, ?)", courses)
    
    # –í–∑–∏–º–∞–º–µ ID-—Ç–∞—Ç–∞ –Ω–∞ –∫—É—Ä—Å–æ–≤–µ—Ç–µ
    cur.execute("SELECT id FROM courses")
    course_ids = [row[0] for row in cur.fetchall()]

    # 3. –°–¢–£–î–ï–ù–¢–ò (10 –±—Ä–æ—è)
    students = [
        ("–ê–ª–µ–∫—Å–∞–Ω–¥—ä—Ä –¢–æ–º–æ–≤", "101", "–ö–°–¢"),
        ("–ë–æ—Ä—è–Ω–∞ –ò–ª–∏–µ–≤–∞", "102", "–ò–ò–¢"),
        ("–í–∞—Å–∏–ª –í–∞—Å–∏–ª–µ–≤", "103", "–ö–°–¢"),
        ("–ì–∞–±—Ä–∏–µ–ª–∞ –ú–∏—Ç–µ–≤–∞", "104", "–°–æ—Ñ—Ç—É–µ—Ä–Ω–æ –ò–Ω–∂."),
        ("–î–∏–º–∏—Ç—ä—Ä –ü–µ–Ω–µ–≤", "105", "–ö–°–¢"),
        ("–ï–º–∏–ª–∏—è –ô–æ—Ä–¥–∞–Ω–æ–≤–∞", "106", "–ò–ò–¢"),
        ("–ñ–∏–≤–∫–æ –°—Ç–æ—è–Ω–æ–≤", "107", "–°–æ—Ñ—Ç—É–µ—Ä–Ω–æ –ò–Ω–∂."),
        ("–ó–ª–∞—Ç–∏–Ω–∞ –ö–æ–ª–µ–≤–∞", "108", "–ö–°–¢"),
        ("–ò–≤–∞–π–ª–æ –ú–∞—Ä–∏–Ω–æ–≤", "109", "–ò–ò–¢"),
        ("–ö–∞—Ç–µ—Ä–∏–Ω–∞ –î–æ–±—Ä–µ–≤–∞", "110", "–°–æ—Ñ—Ç—É–µ—Ä–Ω–æ –ò–Ω–∂.")
    ]
    
    print("üëâ –î–æ–±–∞–≤—è–Ω–µ –Ω–∞ —Å—Ç—É–¥–µ–Ω—Ç–∏...")
    # –ò–∑–ø–æ–ª–∑–≤–∞–º–µ INSERT OR IGNORE, –∑–∞ –¥–∞ –Ω–µ –≥—ä—Ä–º–∏, –∞–∫–æ –ø—É—Å–Ω–µ—à —Å–∫—Ä–∏–ø—Ç–∞ –¥–≤–∞ –ø—ä—Ç–∏
    cur.executemany("INSERT OR IGNORE INTO students (name, fn, major) VALUES (?, ?, ?)", students)
    
    # –í–∑–∏–º–∞–º–µ ID-—Ç–∞—Ç–∞ –Ω–∞ —Å—Ç—É–¥–µ–Ω—Ç–∏—Ç–µ
    cur.execute("SELECT id FROM students")
    student_ids = [row[0] for row in cur.fetchall()]

    # 4. –û–¶–ï–ù–ö–ò (–ì–µ–Ω–µ—Ä–∏—Ä–∞–º–µ –ø–æ 3-4 –æ—Ü–µ–Ω–∫–∏ –Ω–∞ —Å—Ç—É–¥–µ–Ω—Ç)
    print("üëâ –ì–µ–Ω–µ—Ä–∏—Ä–∞–Ω–µ –Ω–∞ —Å–ª—É—á–∞–π–Ω–∏ –æ—Ü–µ–Ω–∫–∏...")
    grades = []
    for s_id in student_ids:
        # –í—Å–µ–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç –ø–æ–ª—É—á–∞–≤–∞ –º–µ–∂–¥—É 3 –∏ 5 –æ—Ü–µ–Ω–∫–∏
        num_grades = random.randint(3, 5)
        # –ò–∑–±–∏—Ä–∞–º–µ —Å–ª—É—á–∞–π–Ω–∏ –ø—Ä–µ–¥–º–µ—Ç–∏, –∫–æ–∏—Ç–æ –µ –∫–∞—Ä–∞–ª
        taken_courses = random.sample(course_ids, num_grades)
        
        for c_id in taken_courses:
            # –ì–µ–Ω–µ—Ä–∏—Ä–∞–º–µ –æ—Ü–µ–Ω–∫–∞ –º–µ–∂–¥—É 3.00 –∏ 6.00
            grade = round(random.uniform(3.00, 6.00), 2)
            grades.append((s_id, c_id, grade))

    cur.executemany("INSERT INTO grades (student_id, course_id, grade) VALUES (?, ?, ?)", grades)

    conn.commit()
    conn.close()
    print("\n‚úÖ –ì–û–¢–û–í–û! –ë–∞–∑–∞—Ç–∞ –¥–∞–Ω–Ω–∏ –µ –ø—ä–ª–Ω–∞ —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è.")
    print("–°–µ–≥–∞ —Å—Ç–∞—Ä—Ç–∏—Ä–∞–π –≥–ª–∞–≤–Ω–æ—Ç–æ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –∏ –≤–∏–∂ —Ä–µ–∑—É–ª—Ç–∞—Ç–∞.")

if __name__ == "__main__":
    fill_database()